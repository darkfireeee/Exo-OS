//! POSIX service catalog covered by the ExoFS translation layer.

use super::roles::{ServiceClass, ServiceStatus, TranslationRole};
use exo_syscall_abi as abi;

#[derive(Clone, Copy, Debug)]
pub struct PosixServiceSpec {
    pub name: &'static str,
    pub syscall: u64,
    pub class: ServiceClass,
    pub role: TranslationRole,
    pub status: ServiceStatus,
}

impl PosixServiceSpec {
    pub const fn core(
        name: &'static str,
        syscall: u64,
        class: ServiceClass,
        role: TranslationRole,
        status: ServiceStatus,
    ) -> Self {
        Self {
            name,
            syscall,
            class,
            role,
            status,
        }
    }
}

