pub fn service_by_syscall(syscall: u64) -> Option<&'static PosixServiceSpec> {
    let mut i = 0usize;
    while i < CORE_POSIX_SERVICES.len() {
        if CORE_POSIX_SERVICES[i].syscall == syscall {
            return Some(&CORE_POSIX_SERVICES[i]);
        }
        i += 1;
    }
    let mut j = 0usize;
    while j < PHASE2_POSIX_SERVICES.len() {
        if PHASE2_POSIX_SERVICES[j].syscall == syscall {
            return Some(&PHASE2_POSIX_SERVICES[j]);
        }
        j += 1;
    }
    None
}
