//! font.rs — Police bitmap PSF intégrée pour l'affichage framebuffer.
//!
//! Police : subset ASCII imprimable (0x20 – 0x7E) + fallback '?' pour les inconnues.
//! Format : 8 pixels de large × 16 pixels de haut.
//! Chaque glyphe = 16 bytes (un byte par ligne, bit 7 = pixel gauche).
//!
//! Encodée directement dans le binaire (`static FONT_DATA`).
//! Aucune dépendance externe — `no_std`.

/// Largeur d'un glyphe en pixels.
pub const FONT_GLYPH_WIDTH: usize = 8;
/// Hauteur d'un glyphe en pixels.
pub const FONT_GLYPH_HEIGHT: usize = 16;

/// Nombre de glyphes dans la police (ASCII 0x20 à 0x7E inclus + fallback).
const FONT_GLYPH_COUNT: usize = (0x7E - 0x20 + 1) + 1; // 95 chars + 1 fallback

/// Retourne le glyphe bitmap pour le caractère ASCII `ch`.
/// Si le caractère n'est pas dans la plage, retourne le glyphe fallback ('?').
#[inline]
pub fn glyph_for(ch: u8) -> &'static [u8; FONT_GLYPH_HEIGHT] {
    let idx = if ch >= 0x20 && ch <= 0x7E {
        (ch - 0x20) as usize
    } else {
        FONT_GLYPH_COUNT - 1 // Fallback
    };
    &FONT_DATA[idx]
}

